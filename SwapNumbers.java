public class SwapNumbers {

    public static void main(String[] args) {

        int num1 = 10, num2 = 20;

        System.out.println("--Before swap--");
        System.out.println("num1 number = " + num1);
        System.out.println("num2 number = " + num2);

        
        int temp = num1;

        num1 = num2;

        num2 = temporary;

        System.out.println("--After swap--");
        System.out.println("num1 number = " + num1);
        System.out.println("num2 number = " + num2);
    }
}